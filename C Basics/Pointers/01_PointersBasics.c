// Pointer is a variable that stores the address of a variable of a certain data type. 
// Pointer is a derived data type. 

// Syntax of Declaring a Pointer: 
// datatype * pointer_name

// Example: 
// int* p;

// Here we cannot simply say that the data type of the p variable is int. A Pointer will contain the address of a variable which is not a value which will be of any datatype like int or float. In this case, p is a pointer vairable that can hold the address of any integer variable. 


